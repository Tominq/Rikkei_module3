package home;

public class VariableExample {
    public static void main(String[] args) {
        int i = 10;
        float f = 20.5f;
        double d = 20.5;
        boolean b = true;
        char c = 'a';
        String s = "Hà Nội";

        System.out.println("Giá trị của biến i: " + i);
        System.out.println("Giá trị của biến f: " + f);
        System.out.println("Giá trị của biến d: " + d);
        System.out.println("Giá trị của biến b: " + b);
        System.out.println("Giá trị của biến c: " + c);
        System.out.println("Giá trị của biến s: " + s);
    }
}